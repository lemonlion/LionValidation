        ///  Equivalent of <![CDATA[ ' ((( ' ]]>
        /// </summary>
        CCC,
        /// <summary>
        ///  Equivalent of <![CDATA[ ' ) ' ]]>
        /// </summary>
        Ɔ,
        /// <summary>
        ///  Equivalent of <![CDATA[ ' )) ' ]]>
        /// </summary>
        ƆƆ,
        /// <summary>
        ///  Equivalent of <![CDATA[ ' ))) ' ]]>
        /// </summary>
        ƆƆƆ,
    }
}
